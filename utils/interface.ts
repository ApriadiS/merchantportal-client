   admin_fee_type: AdminPromoType; // CHANGED from admin_promo_type
   interest_rate: number;
   voucher_code: string;
   is_active: boolean;
   start_date: string; // CHANGED from start_date_promo
   end_date: string; // CHANGED from end_date_promo
   free_installment: number;
